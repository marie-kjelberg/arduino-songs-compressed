int compare(const void *a, const void *b) {
    return (*(int *)a - *(int *)b);
}

int count_unique_sorted(int *arr, int size) {
    if (size == 0) return 0;
    
    // make a copy
    int copy[size];
    memcpy(copy, arr, size * sizeof(arr[0]));
    
    qsort(copy, size, sizeof(int), compare);

    int unique_count = 0;
    bool zeroShite = false;
    for (int i = 1; i < size; i++) {
        // i don't know why, but checking for 0 here is a terrible idea.
        // just believe me
        if (copy[i] == 0) {
            if (zeroShite == false) {
                unique_count++;
                zeroShite = true;
            }
        }
        if (copy[i] < NOTE_B1 || copy[i] > NOTE_DS8) { continue;}
        
        if (copy[i] != copy[i - 1]) {
            unique_count++;
        }
    }
    return unique_count;
}


